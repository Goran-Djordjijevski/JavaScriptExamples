/* Comparison operators and If and If-Else statements */

var isGood = true;

if (isGood === true) {
    console.log("This is true and it's work");
} else {
    console.log("This is false and still works");
}

// Comparison Operators: > < <= >= == !=

var age = 18;

if (age >= 18) {
    console.log("You are an adult");
} else if (age < 18) {
    console.log("You are a child");
}

if (age === 18) {
    console.log("You are 18 years old. Congratulation");
}